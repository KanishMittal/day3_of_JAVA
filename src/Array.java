public class Array {
    public static void main(String[] args) {
        int[] marks = new int[3];
        marks[0] = 97;
        marks[1] = 95;
        marks[2] = 96;
        //OR
        //int marks[] = {97,98,96};
        for (int i = 0; i < 3; i++) {
            System.out.println(marks[i]);
        }
    }
}
